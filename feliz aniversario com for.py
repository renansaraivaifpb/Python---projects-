# feliz animersario para nomes de uma váriavel através de for.

mes = input("mês: ")
if mes == 'dezembro':
    dezembro = ['styles', 'ted', 'scot', 'derik']
    for dezembro in dezembro:
        print(f"Feliz aniversário, {dezembro}")
    print('e é isso.')
